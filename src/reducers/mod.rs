mod pacman;

use crate::reducers::pacman::pacman;
use std::ops::{Add, Div, Mul};
use winit::event::VirtualKeyCode;

pub const MS_PER_UPDATE: f32 = 1.0;

pub fn tick(state: &GameState, last_key: Option<VirtualKeyCode>) -> GameState {
    GameState {
        pacman: pacman(state, last_key),
        time: state.time + 1,
        ..state.clone()
    }
}

#[derive(Clone, Copy)]
pub struct Pos<T> {
    pub x: T,
    pub y: T,
}

pub type Position = Pos<f32>;
pub type Movement = Pos<i8>;

impl Add<Movement> for Position {
    type Output = Position;

    fn add(self, rhs: Movement) -> Self::Output {
        Position {
            x: self.x + rhs.x as f32,
            y: self.y + rhs.y as f32,
        }
    }
}

impl Add<Pos<f32>> for Position {
    type Output = Position;

    fn add(self, rhs: Pos<f32>) -> Self::Output {
        Self::Output {
            x: self.x + rhs.x,
            y: self.y + rhs.y,
        }
    }
}

impl Mul<f32> for Movement {
    type Output = Pos<f32>;

    fn mul(self, rhs: f32) -> Self::Output {
        Self::Output {
            x: self.x as f32 * rhs,
            y: self.y as f32 * rhs,
        }
    }
}

impl Div<i32> for Position {
    type Output = Pos<i32>;

    fn div(self, rhs: i32) -> Self::Output {
        Self::Output {
            x: self.x.round() as i32 / rhs,
            y: self.y.round() as i32 / rhs,
        }
    }
}

impl Position {
    pub fn round(&self) -> Position {
        Position {
            x: self.x.round(),
            y: self.y.round(),
        }
    }
}

#[derive(Clone)]
pub struct PacMan {
    pub pos: Position,
    pub vel: Movement,
}

#[derive(Clone)]
pub struct GameState {
    pub pacman: PacMan,
    pub time: usize,
    pub map: [[u8; 28]; 28],
}

