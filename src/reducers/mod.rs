mod pacman;

use crate::reducers::pacman::pacman;
use winit::event::VirtualKeyCode;

pub const MS_PER_UPDATE: f32 = 1.0;

pub fn tick(state: &GameState, last_key: Option<VirtualKeyCode>) -> GameState {
    GameState {
        pacman: pacman(state, last_key),
        time: state.time + 1,
        ..state.clone()
    }
}

#[derive(Clone, Copy)]
pub struct Pos<T> {
    pub x: T,
    pub y: T,
}

pub type Position = Pos<f32>;
pub type Movement = Pos<i8>;

#[derive(Clone)]
pub struct PacMan {
    pub pos: Position,
    pub vel: Movement,
}

#[derive(Clone)]
pub struct GameState {
    pub pacman: PacMan,
    pub time: usize,
    pub map: [[u8; 28]; 28],
}

